package oops.abstraction;

public class Canara implements RBI{

	@Override
	public void savingAccount() {
		System.out.println("saving account in canara bank");
		
	}

	@Override
	public void currentAccount() {
		System.out.println("current account in canara bank");
		
	}

	@Override
	public void debitCard() {
System.out.println("debit card in canara bank");		
	}

	@Override
	public void creditCard() {
System.out.println("credit card in canara bank");		
	}
	

}
